package acorrer;

public class Podio {

	private int oro, plata,bronce;

	public Podio(int oro, int plata, int bronce) {
		this.oro = oro;
		this.plata = plata;
		this.bronce = bronce;
	}

	public int getOro() {
		return oro;
	}

	public void setOro(int oro) {
		this.oro = oro;
	}

	public int getPlata() {
		return plata;
	}

	public void setPlata(int plata) {
		this.plata = plata;
	}

	public int getBronce() {
		return bronce;
	}

	public void setBronce(int bronce) {
		this.bronce = bronce;
	}
	
	
}
